LES ERREURS COURANTES

Parse error :

-il manque un point virgule
-il manque un guillement, quote, parenthèse
-probleme de concatenation
-accolade mal fermée;

Undefined Function :

La fonction que vous essayez d'utiliser n'existe pas.
-soit la fonction n'existe vraiment pas
-soit une faute de frappe

Wrong parameter count :

-oubli d'un parametre dans la fonction. Exemple :
function appeler(param1, param2, param3);
appeler(param1,param2);