Exemple formulaire :
HTML -> creer les elements (input,etc...)
CSS -> mise en forme (style)
PHP -> Permet d'associer une action supplémentaire qui permet d'exploiter ses informations.

SITE STATIQUE -> un site statique est généralement réalisé avec les langages HTML et CSS. La mise à jour du contenu (image, texte, etc...) vont nécessiter une intervention dans le code.

SITE DYNAMIQUE -> Les informations proviennent d'une base de données, la mise à jour du contenu est réalisée automatiquement grâce à des requêtes envoyé au serveur.
Nous pouvons dire qu'un site dynamique est composé de 2 sites : 
1 partie FRONT (pour l'affichage du site normal) destiné au grand public.
1 partie BACK protégée par authentification et destinée à la mise à jour du contenu par un admin.

